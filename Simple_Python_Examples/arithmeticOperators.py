# Examples with mathematical operators (in other words, mathematical operations)
# Read the theory first

# Addition
print(5 + 5)
print(5 + 5.5) # Works with decimals too

# Subtraction
print(5 - 5)
print(5 - 5.5) # Works with decimals too

# Multiplication
print(5 * 5)
print(5 * 5.5) # Works with decimals too

# Division
print(5 / 5)
print(5 / 5.5) # Works with decimals too

# Integer division
print(5 // 5)
print(5 // 5.5) # Works with decimals too
