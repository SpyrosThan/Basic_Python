# Examples with variables (read the theory first)

# Integer-type variables

a = 5
b = 5
print(a + b)

# Float-type variables
c = 5.5
d = 5.5
print(c + d)

# String-type variables (text)
name = "George"
surname = "Papadopoulos"

# Print a variable
print(name)
print(name, surname)  # This way I can print multiple variables together

print(name + " !random text! " + surname)  # This way I can print variables with random text

# Boolean-type variables (True/False)
x = True
y = False

print(x)
