# Examples with lists
# Creating a list

list1 = [1, 2, 3, 4, 5]
print(list1)

# Displaying a specific element from the list (for example, the 3rd one)
print(list1[2])

# Adding an element to the list
list1.append(6)
print(list1)

# Mathematical operations with list elements
print(list1[2] + list1[3])

# Example with input
number = int(input("Enter a number: "))
list1.append(number)
print(list1)
